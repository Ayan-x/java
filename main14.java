public class main14 {
    public static void main(String[] args) {
        // Logical Operator
        // && (Logiacal AND) -->
        System.out.println((3 > 0) && (5 > 0));
        System.out.println((3 < 0) && (5 > 0));
        System.out.println((3 > 0) && (5 < 0));
        System.out.println((3 < 0) && (5 < 0));
        System.out.println();
        // || (Logical OR) -->
        System.out.println((3 > 0) || (5 > 0));
        System.out.println((3 < 0) || (5 > 0));
        System.out.println((3 > 0) || (5 < 0));
        System.out.println((3 < 0) || (5 < 0));
        System.out.println();
        // ! (Logical NOT) -->
        System.out.println(!(3 > 0));
        System.out.println(!(3 < 0));

    }
}
