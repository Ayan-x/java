public class main28 {
    // Making a square pattern
    public static void main(String[] args) {

        for (int i = 0; i < 4; i++) {
            System.out.println("* * * * ");
        }
        System.out.println();
        int j = 0;
        while (j < 4) {
            System.out.println("* * * *");
            j++;
        }
    }
}
